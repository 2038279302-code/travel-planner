/** 根据目的地关键词模糊匹配 POI 库，未命中返回 null */
export function matchCityPoi(destination: string): CityPoi | null {
  const kw = destination.trim();
  if (!kw) return null;
  const hit = POI_LIBRARY.find((city) =>
    city.aliases.some((alias) => kw.includes(alias) || alias.includes(kw))
  );
  return hit ?? null;
}
